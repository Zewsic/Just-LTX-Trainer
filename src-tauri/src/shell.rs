//! Утилиты для построения shell-скриптов, передаваемых на под через SSH.
//!
//! Все скрипты исполняются под bash. Любые подстановки, которые могут
//! содержать пробелы, юникод или метасимволы (имя проекта, путь, токен),
//! ОБЯЗАНЫ проходить через [`escape`] либо вставляться внутри двойных
//! кавычек (`"{var}"`). Имена tmux-сессий и stat-каталогов строятся через
//! [`safe_name`] и не нуждаются в кавычках.

/// Single-quote escape для bash-аргумента. Безопасно для любого содержимого.
///
/// `foo` → `'foo'`, `it's` → `'it'\''s'`.
pub fn escape(s: &str) -> String {
    format!("'{}'", s.replace('\'', r#"'\''"#))
}

/// Сводит имя к ASCII-alnum + `-_` (для имён tmux-сессий, stat-каталогов).
/// Всё остальное (пробелы, юникод) → `_`.
pub fn safe_name(s: &str) -> String {
    s.chars()
        .map(|c| {
            if c.is_ascii_alphanumeric() || c == '-' || c == '_' {
                c
            } else {
                '_'
            }
        })
        .collect()
}
